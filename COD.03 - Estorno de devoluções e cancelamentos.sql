SELECT * 
FROM VW_AUDIT_RM_ORDENS_VENDA FAT
WHERE 
  FAT.COD_ESTABELECIMENTO = 'R241'
  AND DATA_NOTA_FISCAL BETWEEN '2024-07-01' AND '2025-07-31'
  AND CFOP IN ('1.202', '1.411', '1.661', '1.949', '2.202', '5.916')
  AND (
    NUM_NOTA_FISCAL LIKE '%EST%' 
    OR NUM_NOTA_FISCAL LIKE '%NFD%'
  )
 