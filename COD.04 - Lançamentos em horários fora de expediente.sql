SELECT *, DATENAME(WEEKDAY, DATA_NOTA_FISCAL) AS DIA_SEMANA
FROM VW_AUDIT_RM_ORDENS_VENDA
WHERE 
  COD_ESTABELECIMENTO = 'R241'
  AND DATA_NOTA_FISCAL BETWEEN '2024-07-01' AND '2025-07-31'
  AND CFOP IN ('5.102', '5.405', '5.655', '6.102', '6.404')
  AND DATEPART(WEEKDAY, DATA_NOTA_FISCAL) IN (1, 7)